package com.example.bai8;

public class Hinhanh {
    private String Ten;
    private  int Hinh;

    public Hinhanh(String ten, int hinh) {
        this.Ten = ten;
        Hinh = hinh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }
}
